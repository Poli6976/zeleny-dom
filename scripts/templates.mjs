export function skeletonBody({ title, category }) {
  const sections = SECTIONS[category] ?? SECTIONS.komnatnye;
  const products = DEFAULT_PRODUCTS[category] ?? [];

  const lines = [];
  lines.push(
    `> ✏️ **Черновик-скелет — нужна вычитка перед публикацией.** Наполни каждый раздел ` +
      `конкретными советами, затем поставь \`draft: false\` во frontmatter. Удали эту заметку.`,
  );
  lines.push('');
  lines.push(`Вступление: зацепи читателя и скажи, чем поможет статья «${title}».`);
  lines.push('');
  for (const s of sections) {
    lines.push(`## ${s}`);
    lines.push('');
    lines.push(`_Напиши раздел «${s}» здесь._`);
    lines.push('');
  }
  return { body: lines.join('\n'), products };
}

